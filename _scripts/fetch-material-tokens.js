/**
 * fetch-material-tokens.js
 * Pulls @material/web CSS token files from jsDelivr CDN,
 * extracts --md-sys-* custom properties, maps them to --sc-* tokens,
 * and writes supercss/themes/material.css
 */

const fs   = require("fs");
const path = require("path");

const PACKAGE = "@material/web";
const VERSION = "2.2.0";
const OUT     = path.join(__dirname, "../supercss/themes/material.css");

const JSDELIVR_FILES = `https://data.jsdelivr.com/v1/packages/npm/${encodeURIComponent(PACKAGE)}@${VERSION}/flat`;
const JSDELIVR_CDN   = `https://cdn.jsdelivr.net/npm/${encodeURIComponent(PACKAGE)}@${VERSION}`;

// Token files known to contain MD3 system tokens
const TARGET_PATTERNS = [
  "color-scheme",
  "color.css",
  "shape.css",
  "motion.css",
  "elevation.css",
  "typescale.css",
  "tokens.css",
];

async function fetchText(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.text();
}

function extractCustomProps(css) {
  const props = {};
  const re = /--([\w-]+)\s*:\s*([^;}\n]+)/g;
  let m;
  while ((m = re.exec(css)) !== null) {
    props[`--${m[1].trim()}`] = m[2].trim();
  }
  return props;
}

/** Map MD3 system tokens → --sc-* equivalents */
function buildMapping(tokens) {
  const get = (k, fallback = "") => tokens[k] || fallback;

  // Collect all --md-sys-color-* keys for reference
  const colorKeys = Object.keys(tokens).filter(k => k.startsWith("--md-sys-color-"));

  return {
    // --- Backgrounds & Surfaces ---
    "--sc-bg":        get("--md-sys-color-background",         "#101114"),
    "--sc-surface":   get("--md-sys-color-surface",            "#181a20"),
    "--sc-surface-2": get("--md-sys-color-surface-container",  "#20232b"),
    "--sc-surface-3": get("--md-sys-color-surface-container-high", "#282c36"),

    // --- Text ---
    "--sc-text":     get("--md-sys-color-on-background",       "#f2f3f5"),
    "--sc-text-2":   get("--md-sys-color-on-surface",          "#c8cad0"),
    "--sc-muted":    get("--md-sys-color-on-surface-variant",  "#a4a8b4"),
    "--sc-disabled": get("--md-sys-color-outline",             "#5a5f6e"),

    // --- Borders ---
    "--sc-border":   get("--md-sys-color-outline-variant", "rgba(255,255,255,0.08)"),

    // --- Primary ---
    "--sc-primary":        get("--md-sys-color-primary",          "#8b5cf6"),
    "--sc-primary-hover":  get("--md-sys-color-primary-container","#9d6fff"),
    "--sc-primary-active": get("--md-sys-color-inverse-primary",  "#7c3aed"),

    // --- Shape ---
    "--sc-radius":    get("--md-sys-shape-corner-medium",     "12px"),
    "--sc-radius-sm": get("--md-sys-shape-corner-small",      "8px"),
    "--sc-radius-lg": get("--md-sys-shape-corner-large",      "16px"),

    // --- Motion ---
    "--sc-duration": get("--md-sys-motion-duration-medium2",  "300ms"),
    "--sc-ease":     get("--md-sys-motion-easing-standard",   "cubic-bezier(0.2,0,0,1)"),

    "_colorKeysFound": colorKeys.length,
  };
}

function renderCSS(mapping, rawTokenCount) {
  const meta = mapping["_colorKeysFound"] || 0;
  delete mapping["_colorKeysFound"];

  const lines = Object.entries(mapping)
    .map(([k, v]) => `  ${k}: ${v};`).join("\n");

  return `/* ============================================================
   SUPERCSS — Material Theme
   Auto-generated by _scripts/fetch-material-tokens.js
   Source: @material/web@${VERSION} via jsDelivr CDN
   MD3 tokens found: ${rawTokenCount} (${meta} color tokens mapped)
   ============================================================ */

/* Material Design 3 — dark surface defaults */
html[data-theme="material"] {
${lines}

  /* MD3 elevation shadow tiers */
  --sc-shadow-sm: 0 1px 2px rgba(0,0,0,0.30), 0 1px 3px rgba(0,0,0,0.15);
  --sc-shadow:    0 2px 6px rgba(0,0,0,0.30), 0 1px 2px rgba(0,0,0,0.15);
  --sc-shadow-lg: 0 4px 12px rgba(0,0,0,0.35), 0 2px 4px rgba(0,0,0,0.20);
}

/* Material light mode */
html[data-theme="material"][data-mode="light"] {
  --sc-bg:        #fffbfe;
  --sc-surface:   #fffbfe;
  --sc-surface-2: #f4eff4;
  --sc-surface-3: #ece6f0;
  --sc-text:      #1c1b1f;
  --sc-text-2:    #49454f;
  --sc-muted:     #79747e;
  --sc-border:    rgba(0,0,0,0.08);
  --sc-shadow-sm: 0 1px 2px rgba(0,0,0,0.10);
  --sc-shadow:    0 2px 6px rgba(0,0,0,0.12);
  --sc-shadow-lg: 0 4px 12px rgba(0,0,0,0.15);
}

/* Material motion overrides per density */
html[data-theme="material"][data-motion="subtle"] { --sc-duration: 180ms; }
html[data-theme="material"][data-motion="normal"] { --sc-duration: 300ms; }
html[data-theme="material"][data-motion="none"]   { --sc-duration:   0ms; }
`;
}

async function main() {
  console.log(`[material] Fetching file list for ${PACKAGE}@${VERSION}...`);

  let cssFiles = [];
  try {
    const listRes = await fetch(JSDELIVR_FILES);
    const list    = await listRes.json();
    cssFiles = (list.files || [])
      .map(f => f.name)
      .filter(n => n.endsWith(".css") &&
        TARGET_PATTERNS.some(p => n.toLowerCase().includes(p)));
    console.log(`[material] Found ${cssFiles.length} candidate CSS file(s): ${cssFiles.slice(0,5).join(", ")}`);
  } catch (e) {
    console.warn(`[material] File listing failed: ${e.message} — will use curated defaults`);
  }

  // Fetch and merge all token files
  let merged = {};
  let fetched = 0;

  for (const file of cssFiles.slice(0, 10)) {
    const url = `${JSDELIVR_CDN}${file}`;
    try {
      console.log(`[material] Fetching ${file}...`);
      const text  = await fetchText(url);
      const props = extractCustomProps(text);
      const md3   = Object.fromEntries(
        Object.entries(props).filter(([k]) => k.startsWith("--md-sys-"))
      );
      merged = { ...merged, ...md3 };
      fetched++;
    } catch (e) {
      console.warn(`[material] Skip ${file}: ${e.message}`);
    }
  }

  console.log(`[material] Fetched ${fetched} file(s), ${Object.keys(merged).length} --md-sys-* tokens extracted`);

  const mapping = buildMapping(merged);
  const css     = renderCSS(mapping, Object.keys(merged).length);

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, css, "utf-8");
  console.log(`[material] Written: ${OUT}`);
}

main().catch(e => { console.error(e); process.exit(1); });
